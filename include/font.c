void p_char(char c,int x,int y,unsigned int color,unsigned int *buf,int w,int h)
{
	int x1,y1;
	y1=0;
	if(c<33||c>126)
	{
		return;
	}
	while(y1<16)
	{
		x1=0;
		while(x1<8)
		{
			if(x+x1>=0&&x+x1<w&&y+y1>=0&&y+y1<h)
			{
				if(font_array[((unsigned int)c-33<<4)+y1]&1<<x1)
				{
					buf[w*(y+y1)+x+x1]=color;
				}
			}
			++x1;
		}
		++y1;
	}
}
void p_str(char *str,int l,int x,int y,unsigned int color,unsigned int *buf,int w,int h)
{
	while(l)
	{
		p_char(*str,x,y,color,buf,w,h);
		--l;
		++str;
		x+=8;
	}
}
