void display_icon(char *icon,int x,int y)
{
	int x1,y1;
	char c;
	unsigned int *pixel;
	y1=0;
	while(y1<16)
	{
		x1=0;
		while(x1<16)
		{
			if(x+x1>=0&&x+x1<WINW&&y+y1>=0&&y+y1<WINH)
			{
				c=*icon;
				pixel=pbuf+(y+y1)*WINW+x+x1;
				if(c=='1')
				{
					*pixel=0;
				}
				else if(c=='2')
				{
					*pixel=0xc0c0c0;
				}
				else if(c=='3')
				{
					*pixel=0x606060;
				}
				else if(c=='4')
				{
					*pixel=0x808000;
				}
				else if(c=='5')
				{
					*pixel=0xffff00;
				}
				else if(c=='6')
				{
					*pixel=0x00c0ff;
				}
				else if(c=='7')
				{
					*pixel=0xffffff;
				}
				else if(c=='8')
				{
					*pixel=0x00e010;
				}
				else if(c=='9')
				{
					*pixel=0xff0000;
				}
			}
			++icon;
			++x1;
		}
		++y1;
	}
}
